# Variable = A variable is a container that stores a value ( String, Integer, Float, Boolean ).
# Python is a dynamically typed language, so we don't need to define the type of the variable while declaring it.
# The declaration happens automatically when we assign a value to a variable.

# Strings
first_name = "Mrudula"
last_name = "Gattupalli"
print(f"Hello {first_name} {last_name}")

# Integer
age = 20
print(f"You are {age} years old")

# Float
price = 10.5
gpa = 3.5
distance = 100.5
print(f"The price of the product is ${price}")
print(f"Your GPA is {gpa}")
print(f"The distance is {distance} miles")

# Boolean
is_student = True
is_teacher = False
print(f"Are you a student? {is_student}")
print(f"Are you a teacher? {is_teacher}")